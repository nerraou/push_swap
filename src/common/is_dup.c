#include "common.h"

int is_dup(int ac, char *av[])
{
	int i;
	int j;

	i = 1;
	j = 2;
	while (i < ac)
	{
		j = i + 1;
		while (j < ac)
		{
			if (ft_strcmp(av[i], av[j]) == 0)
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}