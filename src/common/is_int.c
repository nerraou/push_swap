#include "common.h"

int	is_int(const char *str)
{
	int	i;

	i = 0;
	if (!str[i])
		return (0);
	if (str[0] == '+' || str[0] == '-')
		i++;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
